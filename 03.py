# WAP to add integer to a Floating-Point number.
number=int(input("Enter you integer number : "))
fnumber=float(input("Enter your floating point number : "))
print(f"Addition of integer and floating numbers : {number+fnumber}")

# output:-
#         Enter you integer number : 5
#         Enter your floating point number : 5.8
#         Addition of integer and floating numbers : 10.8